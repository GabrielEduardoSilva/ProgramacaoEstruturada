#include <stdio.h>

void somarColunas(int matriz[61][10]) {
    // Inicialize a 61ª linha com zeros
    for (int j = 0; j < 10; j++) {
        matriz[60][j] = 0;
    }

    // Soma individualmente as colunas e armazena o resultado na 61ª linha
    for (int i = 0; i < 60; i++) {
        for (int j = 0; j < 10; j++) {
            matriz[60][j] += matriz[i][j];
        }
    }
}

